import streamlit as st
import matplotlib.pyplot as plt
import matplotlib.patches as patches

# Ustawienie tytu≈Çu aplikacji Streamlit
st.title("üéÖ ≈öwiƒôty Miko≈Çaj w Streamlit (Rysunek Matplotlib)")

# Utworzenie figury i osi Matplotlib
fig, ax = plt.subplots(figsize=(6, 8))
ax.set_xlim(0, 10)
ax.set_ylim(0, 12)
ax.set_aspect('equal', adjustable='box')
ax.axis('off') # Ukrycie osi

# --- 1. Cia≈Ço Miko≈Çaja (Czerwony Str√≥j) ---
# Tors (prostokƒÖt)
# Ulepszenie: Lepszy kolor czerwony
ax.add_patch(patches.Rectangle((3.5, 3.5), 3, 4, color='#DC143C', alpha=0.9))

# Pas (czarny prostokƒÖt)
ax.add_patch(patches.Rectangle((3.5, 5.0), 3, 0.5, color='black'))

# Klamra pasa (≈º√≥≈Çty/z≈Çoty kwadrat)
ax.add_patch(patches.Rectangle((4.7, 5.0), 0.6, 0.5, color='#FFD700'))

# --- 2. G≈Çowa i Broda ---
# G≈Çowa (ko≈Ço)
ax.add_patch(patches.Circle((5, 8.5), 1, color='#F0CC9C')) # Podniesienie g≈Çowy

# Broda (owal/ko≈Ço pod g≈ÇowƒÖ)
# Ulepszenie: Lepszy kszta≈Çt brody
ax.add_patch(patches.Ellipse((5, 7.5), 3, 2, color='white', zorder=2))
ax.add_patch(patches.Circle((5, 7.5), 0.5, color='white', zorder=3)) # Dodatkowa bia≈Ça "kƒôpka" na nos i policzki

# Oczy (ma≈Çe czarne kropki)
ax.add_patch(patches.Circle((4.5, 8.7), 0.1, color='black'))
ax.add_patch(patches.Circle((5.5, 8.7), 0.1, color='black'))

# --- 3. Czapka ---
# Czerwony sto≈ºek czapki (tr√≥jkƒÖt)
# Ulepszenie: Kszta≈Çt czapki pochylony
ax.add_patch(patches.Polygon([[4, 9.5], [6.2, 9.5], [5.8, 11.5]], closed=True, color='red'))

# Bia≈Çy pasek na dole czapki (prostokƒÖt)
ax.add_patch(patches.Rectangle((3.8, 9.3), 2.6, 0.5, color='white'))

# Bia≈Çy pompon na czubku czapki (ko≈Ço)
ax.add_patch(patches.Circle((5.8, 11.5), 0.45, color='white'))

# --- 4. Rƒôce i Buty (Dodane dla lepszego wyglƒÖdu) ---
# Buty (dwa czarne prostokƒÖty)
ax.add_patch(patches.Rectangle((4.0, 3.0), 0.7, 0.5, color='black'))
ax.add_patch(patches.Rectangle((5.3, 3.0), 0.7, 0.5, color='black'))

# Rƒôce (dwa czerwone ko≈Ça)
ax.add_patch(patches.Circle((3.2, 6.5), 0.5, color='red')) # Lewa rƒôka
ax.add_patch(patches.Circle((6.8, 6.5), 0.5, color='red')) # Prawa rƒôka

# --- 5. Prezenty (Worek) ---
# Worek na prezenty (wiƒôkszy, zielony prostokƒÖt, z ty≈Çu)
ax.add_patch(patches.Rectangle((6.0, 3.5), 2.5, 3.5, color='#006400', alpha=0.9, zorder=1))

# WystajƒÖce prezenty (zmienione kolory i rozmieszczenie)
ax.add_patch(patches.Rectangle((6.3, 7.0), 0.6, 0.6, color='#FF4500')) # Pomara≈Ñczowy
ax.add_patch(patches.Rectangle((7.0, 7.2), 0.7, 0.7, color='#1E90FF')) # Niebieski
ax.add_patch(patches.Rectangle((7.8, 6.8), 0.5, 0.5, color='#DAA520')) # Z≈Çoty
ax.add_patch(patches.Rectangle((6.8, 7.8), 0.5, 0.5, color='#3CB371')) # Ciemnozielony

# Tekst ≈õwiƒÖteczny
ax.text(5, 1.5, "Ho Ho Ho!", fontsize=20, ha='center', color='#DC143C', fontweight='bold')
ax.text(5, 0.8, "Weso≈Çych ≈öwiƒÖt!", fontsize=14, ha='center', color='black')

# Wy≈õwietlenie obrazka w Streamlit
st.pyplot(fig)